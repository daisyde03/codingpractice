#problem statement:

#Given two integers num1 and num2, return the sum of the two integers.
#https://leetcode.com/problems/add-two-integers/

class Solution(object):
    def sum(self, num1, num2):
        """
        :type num1: int
        :type num2: int
        :rtype: int
        """
        return(num1+num2)


obj = Solution()
a=10
b=15
print(obj.sum(a,b))
